    @BeforeEach
    fun initSolver() {
        solver = ScrapSolver(mathMock)
    }

    @Test
    fun `at asymptotes`() {
        // periodic
        Assertions.assertEquals(NaN, solver.solve(0.0))
        Assertions.assertEquals(NaN, solver.solve(-0.5 * PI))
        Assertions.assertEquals(NaN, solver.solve(PI))
        Assertions.assertEquals(NaN, solver.solve(-3 / 2 * PI))
        Assertions.assertEquals(NaN, solver.solve(0.0 - PERIOD))
        Assertions.assertEquals(NaN, solver.solve(-0.5 * PI - PERIOD))
        Assertions.assertEquals(NaN, solver.solve(PI - PERIOD))
        Assertions.assertEquals(NaN, solver.solve(-3 / 2 * PI - PERIOD))
    }

    @Test
    fun `near asymptotes`() {
        assertBigDouble(2.3799959934542757E7,   solver.solve(0.0     + NEIGHBORHOOD_DELTA))
        assertEquals(NaN,                       solver.solve(-0.5*PI + NEIGHBORHOOD_DELTA))
        assertEquals(NaN,                       solver.solve(-PI     + NEIGHBORHOOD_DELTA))
        assertEquals(NaN,                       solver.solve(-3/2*PI + NEIGHBORHOOD_DELTA))
        assertEquals(NaN,                       solver.solve(0.0     - NEIGHBORHOOD_DELTA))
